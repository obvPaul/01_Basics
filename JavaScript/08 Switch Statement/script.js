// Erstelle eine Zufallszahl zwischen 5 und 10
let randomNumber = Math.random() * 5 + 5;
let number = Math.floor(randomNumber);

console.log(number);

// Wenn der Wert 10 ist gib aus Ten 
if(number==10){
    console.log("Ten")
}
// Wenn der Wert 9 ist gib aus Nine 
if(number==9){
    console.log("Nine")
}
// Wenn der Wert 8 ist gib aus Eight 
if(number==8){
    console.log("Eight")
}
// etc. 
if(number==7){
    console.log("Seven")
}
if(number==6){
    console.log("Six")
}
